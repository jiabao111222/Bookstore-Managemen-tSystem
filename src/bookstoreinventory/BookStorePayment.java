    private void bcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_bcodeKeyPressed

        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            try{

                con = Connections.getConnection();
                String Bcode = bcode.getText();

                pst = con.prepareStatement("select * from book where book_id = ?");     //prepare statement get from database book table

                pst.setString(1,Bcode);         //set parameter
                rs = pst.executeQuery();
                
                if(rs.next() == false)
                {
                    JOptionPane.showMessageDialog(this, "Book code not Found");
                }
                
                else
                {
                    String Bname = rs.getString("title");       //from title
                    bname.setText(Bname.trim());
                    
                    String Price = rs.getString("price");
                    price.setText(Price.trim());
                    
                    qty.requestFocus();         //guide user
                }
                
            } catch (HeadlessException | SQLException ex){
                System.out.println(ex);
            }
        }
   
    }//GEN-LAST:event_bcodeKeyPressed

