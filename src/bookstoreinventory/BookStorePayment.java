public void sales(String Bookname) {
    
    String totalcost = tcost.getText();
    String bpay = pay.getText();
    String bbalance = balance.getText();

    int lastid = 0;

    try {
        con = Connections.getConnection();

        String query = "INSERT INTO sales (subtotal, pay, bal) VALUES (?, ?, ?)";
        pst = con.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
        pst.setString(1, totalcost);
        pst.setString(2, bpay);
        pst.setString(3, bbalance);
        pst.executeUpdate();

        rs = pst.getGeneratedKeys();
        if (rs.next()) {
            lastid = rs.getInt(1);
        }

        double totalCostValue = Double.parseDouble(totalcost);
        double paymentValue = Double.parseDouble(bpay);
        if (paymentValue < totalCostValue) {
            int confirm = JOptionPane.showConfirmDialog(this, "Warning, Payment not enough.", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }
        }

        String insertQuery = "INSERT INTO sales_product (sales_id, bname, price, qty, total) VALUES (?, ?, ?, ?, ?)";
        pst1 = con.prepareStatement(insertQuery);

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        for (int j = 0; j < model.getRowCount(); j++) {
            String bname = model.getValueAt(j, 0).toString();
            String price = model.getValueAt(j, 1).toString();
            String qty = model.getValueAt(j, 2).toString();
            String total = model.getValueAt(j, 3).toString();

            int soldQty = Integer.parseInt(qty);

            String stockQuery = "SELECT Qty FROM book WHERE title = ?";
            PreparedStatement stockPst = con.prepareStatement(stockQuery);
            stockPst.setString(1, bname);
            ResultSet stockRs = stockPst.executeQuery();

            if (stockRs.next()) {
                int availableQty = stockRs.getInt(1);
                if (availableQty < soldQty) {
                    JOptionPane.showMessageDialog(this, "Out of stock for " + bname);
                    return; 
                }
            }

            pst1.setInt(1, lastid);
            pst1.setString(2, bname);
            pst1.setString(3, price);
            pst1.setInt(4, soldQty);
            pst1.setString(5, total);
            pst1.executeUpdate();

            String updateStockQuery = "UPDATE book SET Qty = Qty - ? WHERE title = ?";
            PreparedStatement updateStockPst = con.prepareStatement(updateStockQuery);
            updateStockPst.setInt(1, soldQty);
            updateStockPst.setString(2, bname);
            updateStockPst.executeUpdate();
        }

        JOptionPane.showMessageDialog(this, "Sales completed successfully ^_^");
    } catch (SQLException ex) {
        Logger.getLogger(BookStorePayment.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error while performing sales. Please check logs for details.");
    } finally {

        try {
            if (pst1 != null) {
                pst1.close();
            }
            if (rs != null) {
                rs.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(BookStorePayment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
}
